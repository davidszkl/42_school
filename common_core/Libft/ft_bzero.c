#include "libft.h"
#include <stdlib.h>

void	ft_bzero(void	*s, size_t	n)
{
	unsigned char	*ptr;

	if (!s)
		return ;
	ptr = s;
	while (n-- > 0)
		*ptr++ = 0;
}
